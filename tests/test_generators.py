class TestPulseStubGenerator:
    def test_generates_function(self, contrato_program):
        pulse = contrato_program.get_pulse("ExtrairRisco")
        output = generate_pulse_stub(pulse, contrato_program, "nextjs")
        assert "export async function extrairRisco" in output

    def test_includes_ai_metadata(self, contrato_program):
        pulse = contrato_program.get_pulse("ExtrairRisco")
        output = generate_pulse_stub(pulse, contrato_program, "nextjs")
        assert "AI.scan()" in output
        assert "temperature: 0.1" in output
        assert "@prompts/legal_scan.md" in output

    def test_guard_as_comment(self, contrato_program):
        pulse = contrato_program.get_pulse("ExtrairRisco")
        output = generate_pulse_stub(pulse, contrato_program, "nextjs")
        assert "Guard: Guard.SomenteAdvogados" in output
        assert "enforced at pipeline route level" in output

    def test_no_guard_reference_when_absent(self, contrato_program):
        pulse = contrato_program.get_pulse("Notificar")
        output = generate_pulse_stub(pulse, contrato_program, "nextjs")
        assert "Guard:" not in output


