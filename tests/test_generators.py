# ============================================================
# Routes Generator (Layer 2)
# ============================================================

class TestRoutesGenerator:
    def test_generates_route_file(self, contrato_program):
        files = generate_routes(contrato_program, "nextjs")
        assert "app/api/analisar-contrato/route.ts" in files

    def test_route_imports_pulses(self, contrato_program):
        files = generate_routes(contrato_program, "nextjs")
        content = files["app/api/analisar-contrato/route.ts"]
        assert "import { extrairRisco }" in content
        assert "import { gerarResumo }" in content
        assert "import { notificar }" in content

    def test_route_imports_guards(self, contrato_program):
        files = generate_routes(contrato_program, "nextjs")
        content = files["app/api/analisar-contrato/route.ts"]
        assert "import { somenteAdvogados }" in content

    def test_route_chains_results(self, contrato_program):
        files = generate_routes(contrato_program, "nextjs")
        content = files["app/api/analisar-contrato/route.ts"]
        # Step 2 receives result from step 1
        assert "gerarResumo(extrairRiscoResult)" in content
        # Step 3 receives result from step 2
        assert "notificar(gerarResumoResult)" in content

    def test_route_has_retry(self, contrato_program):
        files = generate_routes(contrato_program, "nextjs")
        content = files["app/api/analisar-contrato/route.ts"]
        assert "MAX_RETRIES = 3" in content
        assert "for (let attempt" in content
        assert "Math.pow(2, attempt)" in content  # exponential backoff

    def test_route_has_error_handling(self, contrato_program):
        files = generate_routes(contrato_program, "nextjs")
        content = files["app/api/analisar-contrato/route.ts"]
        assert "catch (error)" in content
        assert "Pipeline failed after retries" in content


