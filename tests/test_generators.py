from epi.generators.epistemic.ai_scan import generate_pulse_stub
from epi.generators.epistemic.lens_mood import generate_lens_stub


EXAMPLES_DIR = Path(__file__).parent.parent / "examples"


@pytest.fixture(scope="module")
def contrato_program():
    source = (EXAMPLES_DIR / "contrato.epi").read_text()
    return parse_epi(source)


# ============================================================
# Prisma Generator (Layer 2)
# ============================================================

class TestPrismaGenerator:
    def test_generates_valid_prisma(self, contrato_program):
        output = generate_prisma(contrato_program)
        assert "model Contrato {" in output
        assert "model Advogado {" in output

    def test_rigid_uuid_auto(self, contrato_program):
        output = generate_prisma(contrato_program)
        assert "id String @id @default(uuid())" in output

    def test_rigid_datetime_auto(self, contrato_program):
        output = generate_prisma(contrato_program)
        assert "criado_em DateTime @default(now())" in output

    def test_rigid_decimal(self, contrato_program):
        output = generate_prisma(contrato_program)
        assert "valor Decimal" in output

    def test_epistemic_field_annotation(self, contrato_program):
        output = generate_prisma(contrato_program)
        assert "risco String // @epi:epistemic AI.Enum(Alto, Medio, Baixo)" in output

    def test_epistemic_text_annotation(self, contrato_program):
        output = generate_prisma(contrato_program)
        assert "especialidade String // @epi:epistemic AI.Text" in output

    def test_header_present(self, contrato_program):
        output = generate_prisma(contrato_program)
        assert "Auto-generated by Epi Transpiler v0.2" in output
        assert 'provider = "postgresql"' in output


