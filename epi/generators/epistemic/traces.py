"""
Epistemic Generator: Trace → Inspect/Resume infrastructure

Generates three artefacts for each Pulse with Traces:

1. lib/trace-store.ts        — in-memory state store (swap for Redis/DB in prod)
2. traces/{pulse}/{trace}.ts — trace execution with confidence extraction
3. app/api/traces/{pulse}/[traceId]/inspect/route.ts
4. app/api/traces/{pulse}/[traceId]/resume/route.ts

Design rationale:
- Inspect route: Professor (or any authorized role) can read intermediate
  reasoning steps BEFORE the AI sees subsequent inputs.
- Resume route: After review, Professor approves or corrects the reasoning.
  The correction becomes the context for the next Trace in the Pulse.
- Confidence is extracted from the AI response via a mandatory _confidence
  field injected into the system prompt. This implements Dante Barone's
  stochastic routing: low confidence → automatic pause → human review.

Source: Rosa (epistemic breakpoints) + Dante (stochastic routing).
"""

from __future__ import annotations

from epi.parser.ast_nodes import EpiProgram, Pulse, TraceStep


