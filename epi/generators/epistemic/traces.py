def _inspect_route(pulse: Pulse, guard_role: str) -> str:
    pulse_pascal = pulse.name
    return (
        f"// Inspect route — Trace epistemic state\n"
        f"// Auto-generated by Epi Transpiler v0.3\n"
        f"// Authorized role: {guard_role}\n"
        f"//\n"
        f"// GET /api/traces/{_to_kebab(pulse.name)}/[traceId]/inspect\n"
        f"// Returns the current TraceState including exposed reasoning fields.\n"
        f"// Use this endpoint to read the AI's intermediate reasoning\n"
        f"// before deciding whether to approve or correct it.\n"
        f"\n"
        f'import {{ NextRequest, NextResponse }} from "next/server";\n'
        f'import {{ getServerSession }} from "next-auth";\n'
        f'import {{ getTrace }} from "../../../../../lib/trace-store";\n'
        f"\n"
        f"export async function GET(\n"
        f"  request: NextRequest,\n"
        f"  {{ params }}: {{ params: {{ traceId: string }} }}\n"
        f") {{\n"
        f'  const session = await getServerSession();\n'
        f'  if (!session) {{\n'
        f'    return NextResponse.json({{ error: "Unauthorized" }}, {{ status: 401 }});\n'
        f'  }}\n'
        f"\n"
        f"  const state = getTrace(params.traceId);\n"
        f"  if (!state) {{\n"
        f'    return NextResponse.json({{ error: "Trace not found" }}, {{ status: 404 }});\n'
        f"  }}\n"
        f"\n"
        f"  return NextResponse.json({{\n"
        f"    traceId: state.traceId,\n"
        f"    pulseName: state.pulseName,\n"
        f"    traceName: state.traceName,\n"
        f"    status: state.status,\n"
        f"    exposedFields: state.exposedFields,\n"
        f"    confidence: state.confidence,\n"
        f"    confidenceThreshold: state.confidenceThreshold,\n"
        f"    pausedAt: state.pausedAt,\n"
        f"  }});\n"
        f"}}\n"
    )


def _resume_route(pulse: Pulse, guard_role: str) -> str:
    pulse_kebab = _to_kebab(pulse.name)
    # Build imports for all trace execution modules
    trace_imports = "\n".join(
        f'import {{ execute{t.name} }} from "../../../../../traces/{pulse_kebab}/{_to_kebab(t.name)}";'
        for t in pulse.traces
    )
    # Build dispatch map
    dispatch_entries = "\n".join(
        f'    "{t.name}": execute{t.name},'
        for t in pulse.traces
    )

    return (
        f"// Resume route — Continue paused trace after human review\n"
        f"// Auto-generated by Epi Transpiler v0.3\n"
        f"// Authorized role: {guard_role}\n"
        f"//\n"
        f"// POST /api/traces/{pulse_kebab}/[traceId]/resume\n"
        f"// Body: {{ correction?: Record<string, unknown> }}\n"
        f"//\n"
        f"// If correction is provided, it replaces the AI's raw output\n"
        f"// and becomes the context for the next Trace in the Pulse.\n"
        f"// If no correction, the original AI output is accepted as-is.\n"
        f"\n"
        f'import {{ NextRequest, NextResponse }} from "next/server";\n'
        f'import {{ getServerSession }} from "next-auth";\n'
        f'import {{ getTrace, updateTrace }} from "../../../../../lib/trace-store";\n'
        f"{trace_imports}\n"
        f"\n"
        f"export async function POST(\n"
        f"  request: NextRequest,\n"
        f"  {{ params }}: {{ params: {{ traceId: string }} }}\n"
        f") {{\n"
        f"  const session = await getServerSession();\n"
        f'  if (!session) {{\n'
        f'    return NextResponse.json({{ error: "Unauthorized" }}, {{ status: 401 }});\n'
        f'  }}\n'
        f"\n"
        f"  const state = getTrace(params.traceId);\n"
        f"  if (!state) {{\n"
        f'    return NextResponse.json({{ error: "Trace not found" }}, {{ status: 404 }});\n'
        f"  }}\n"
        f'  if (state.status !== "paused") {{\n'
        f'    return NextResponse.json({{ error: "Trace is not paused", status: state.status }}, {{ status: 400 }});\n'
        f"  }}\n"
        f"\n"
        f"  const body = await request.json().catch(() => ({{}}));\n"
        f"  const correction = body.correction ?? null;\n"
        f'  const resumedBy = (session as {{ user?: {{ email?: string }} }}).user?.email ?? "unknown";\n'
        f"\n"
        f"  // Accept correction or original AI output\n"
        f"  const acceptedOutput = correction ?? state.rawOutput;\n"
        f"\n"
        f"  updateTrace(params.traceId, {{\n"
        f'    status: "completed",\n'
        f"    resumedBy,\n"
        f"    humanCorrection: correction ?? undefined,\n"
        f"  }});\n"
        f"\n"
        f"  return NextResponse.json({{\n"
        f"    success: true,\n"
        f"    traceId: params.traceId,\n"
        f"    resumedBy,\n"
        f"    acceptedOutput,\n"
        f"    corrected: correction !== null,\n"
        f"  }});\n"
        f"}}\n"
    )


