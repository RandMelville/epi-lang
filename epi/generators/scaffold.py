def generate_readme(program: EpiProgram, target: str) -> str:
    name = next(
        (m.value for m in program.metadata if m.key.lower() == "name"),
        "Epi Generated App",
    )
    framework = "Next.js + Prisma + Anthropic SDK" if target == "nextjs" else "FastAPI + Prisma + Anthropic SDK"
    return f"""\
# {name}

Generated by [Epi Transpiler](https://github.com/RandMelville/epi-lang) v0.3.

## Stack

{framework}

## Setup

```bash
# 1. Install dependencies
npm install

# 2. Configure environment
cp .env.example .env
# Edit .env and set DATABASE_URL and ANTHROPIC_API_KEY

# 3. Initialize database
npx prisma migrate dev --name init

# 4. Run development server
npm run dev
```

## Project Structure

```
├── app/api/          # Next.js API routes (generated from Pipelines)
├── middleware/       # Auth guards (generated from Guards)
├── pulses/           # AI inference functions (generated from Pulses)
├── components/       # UI components (generated from Lenses)
├── validators/       # Zod schemas for epistemic type validation
├── lib/              # Trace infrastructure (inspect/resume)
├── prompts/          # Prompt files referenced by Pulses
└── prisma/           # Database schema (generated from Entities)
```

## Epistemic Types

This project uses the Epi Epistemic Type System. AI-inferred fields are validated
at runtime via Zod schemas in `validators/`. See [SPEC.md](https://github.com/RandMelville/epi-lang/blob/main/SPEC.md).
"""
